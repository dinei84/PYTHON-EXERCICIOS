def somar(a, b):
    resultado = a + b
    return resultado


# Chamando a função
resultado_soma = somar(10, 5)
print("Resultado da soma:", resultado_soma)


# Funções:
# Uma função também é um bloco de código, mas com a diferença crucial de que ela retorna um valor.
# As funções são usadas para processar dados e produzir resultados que podem ser usados por outros algoritmos.
# Nesse exemplo, a função somar recebe dois argumentos (a e b), calcula a soma e retorna o resultado