# Função len() para obter o tamanho de uma lista
minha_lista = [1, 2, 3, 4, 5]
tamanho_lista = len(minha_lista)
print("Tamanho da lista:", tamanho_lista)

# Função max() para encontrar o valor máximo em uma lista
maior_valor = max(minha_lista)
print("Maior valor da lista:", maior_valor)


