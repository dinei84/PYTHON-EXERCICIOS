def exibir_mensagem():
    print("Olá, mundo!")

# Chamando o procedimento
exibir_mensagem()

# Procedimentos:
# Um procedimento é um bloco de código que executa uma sequência de ações sem retornar um valor específico.
# Geralmente, os procedimentos são usados para realizar tarefas específicas
# Nesse exemplo, o procedimento exibir_mensagem simplesmente imprime a mensagem “Olá, mundo!”.